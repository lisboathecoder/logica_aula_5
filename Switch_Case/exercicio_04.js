let nota = 10;

switch (true) {
    case nota >= 9:
        console.log("A");
        break;
    case nota >= 7:
        console.log("B");
        break;
    case nota >= 5:
        console.log("C");
        break;
    case nota >= 3:
        console.log("D");
        break;
    case nota >= 2:
        console.log("E");
        break;
    default:
        console.log("Número inválido.");
}